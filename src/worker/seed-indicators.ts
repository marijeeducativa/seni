// I'll create a simplified version that loads from the structured data
export async function seedIndicators(db: D1Database) {
  // First, create categories if they don't exist
  const categories = [
    "Relaciones socioafectivas, identidad y autonomía",
    "Lenguaje e Interacción",
    "Lenguaje e interacción",
    "Descubrimiento de su cuerpo y su relación con el entorno",
    "Dominio Socioemocional",
    "Dominio Artístico y Creativo",
    "Dominio Psicomotor y de Salud",
    "Dominio Descubrimiento del Mundo",
    "Dominio Cognitivo",
    "Dominio Comunicativo",
  ];

  const categoryMap: Record<string, number> = {};

  for (const catName of categories) {
    const existing = await db.prepare(
      "SELECT id FROM categorias_indicadores WHERE nombre_categoria = ?"
    ).bind(catName).first();

    if (!existing) {
      const result = await db.prepare(
        "INSERT INTO categorias_indicadores (nombre_categoria) VALUES (?)"
      ).bind(catName).run();
      categoryMap[catName] = result.meta.last_row_id as number;
    } else {
      categoryMap[catName] = (existing as any).id;
    }
  }

  // Now insert all indicators
  for (const indicator of INDICATORS_SEED) {
    const catId = categoryMap[indicator.categoria];

    await db.prepare(
      `INSERT INTO indicadores (descripcion, id_categoria, niveles_aplicables, tipo_evaluacion, orden)
       VALUES (?, ?, ?, ?, ?)`
    ).bind(
      indicator.descripcion,
      catId,
      indicator.nivel,
      "cualitativa",
      indicator.orden
    ).run();
  }
}
